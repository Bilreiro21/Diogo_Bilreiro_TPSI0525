// Operadores aritméticos (+, -, *, /, %, ** exponencial, ++, -- )

console.log(2 + 4); // Soma
console.log(2 - 4); // Subtração
console.log(2 * 4); // Multiplicação
console.log(2 / 4); // Divisão
console.log(2 % 4); // Resto da divisão
console.log(2 ** 4); // Exponencial
console.log(2++); // Incremento
console.log(2--); // Decremento

